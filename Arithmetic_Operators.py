# Declare two variables
a = 10
b = 5

# Addition
sum_result = a + b
print("Sum:", sum_result)

# Subtraction
difference = a - b
print("Difference:", difference)

# Multiplication
product = a * b
print("Product:", product)

# Division
quotient = a / b
print("Quotient:", quotient)

# Modulus (remainder)
remainder = a % b
print("Remainder:", remainder)
